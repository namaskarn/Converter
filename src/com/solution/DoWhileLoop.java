package com.solution;

//write a programe to print sum of  1 to 10 numbers using  do-while loop

public class DoWhileLoop {
	
	public static void main(String[] args) {
		int sum =0;
		
		int i =1;
		
		do {
			sum = sum + i;
			i++;
		
			
		} while (i<=10);
		
		
		System.out.println(sum);//55
		
		
		
		 

	}

}
